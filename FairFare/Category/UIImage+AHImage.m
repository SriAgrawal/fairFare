+ (UIImage *)ah_imageWithSize:(CGSize)size renderer:(renderToContext)renderer {
    return [UIImage ah_imageWithSize:size opaque:NO renderer:renderer];
}

+ (UIImage *)ah_imageWithSize:(CGSize)size opaque:(BOOL)opaque renderer:(renderToContext)renderer {
    UIImage *image;
    
    UIGraphicsBeginImageContextWithOptions(size, opaque, 0);
    
    CGContextRef context = UIGraphicsGetCurrentContext();
    
    CGRect imageRect = CGRectMake(0.f, 0.f, size.width, size.height);
    
    renderer(context, imageRect);
    
    image = UIGraphicsGetImageFromCurrentImageContext();
    UIGraphicsEndImageContext();
    return image;
}

